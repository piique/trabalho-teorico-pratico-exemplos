
def stringify(caracteres):
    string = ''
    for c in caracteres:
        string = string + c
    return string


caracteres = ['P', 'y', 't', 'h', 'o', 'n']
string_final = stringify(caracteres)
print("String final: ", string_final)  # String final: Python

